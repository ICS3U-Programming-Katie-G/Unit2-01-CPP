// this program calculates the area and circumference
// of a circle with a set radius of 4.


#include <iostream>
#include <cmath>

int main() {
    std::cout << "A circle has the radius of 4:\n";
    std::cout << "The area is: " << M_PI * pow(4, 2) << " mm^2\n";
    std::cout << "The circumference is: " << 2 * M_PI * 4 << " mm\n";
}
